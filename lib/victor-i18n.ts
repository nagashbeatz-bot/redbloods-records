"use client";

/**
 * Local i18n for the Victor page ONLY (components/team/VictorProfilePage.tsx).
 * Not a global app i18n — the sidebar / AppShell / nav stay Hebrew always.
 *
 * - Language is persisted in localStorage (rb_victor_lang), default "he".
 * - Missing keys fall back to Hebrew, so partial coverage never breaks the UI.
 * - Status values from the DB stay canonical Hebrew; statusLabel() only changes
 *   the DISPLAYED label. Never submit a translated value to the API.
 */
import { useEffect, useLayoutEffect, useState } from "react";

export type VictorLang = "he" | "en" | "ru";
export const VICTOR_LANGS: { id: VictorLang; label: string }[] = [
  { id: "he", label: "עברית" },
  { id: "en", label: "English" },
  { id: "ru", label: "Русский" },
];

const KEY = "rb_victor_lang";
const ROLE_KEY = "rb_victor_role"; // cached role so read() can pick the right default before paint
const EVENT = "rb:victor-lang";
const useIso = typeof window !== "undefined" ? useLayoutEffect : useEffect;

// Victor's experience is English/Russian only (never Hebrew). Owner: he/en/ru.
function read(): VictorLang {
  try {
    const v = localStorage.getItem(KEY);
    if (v === "en" || v === "ru") return v;
    // "he" or absent → he for owner/unknown, en for Victor.
    return localStorage.getItem(ROLE_KEY) === "victor" ? "en" : "he";
  } catch {
    return "he";
  }
}

/** Default language for a role: Victor → en, owner/unknown → he. */
export function victorDefaultLang(role?: string | null): VictorLang {
  return role === "victor" ? "en" : "he";
}

/** Languages the role may choose: Victor → en/ru only (no Hebrew), owner → all. */
export function allowedVictorLangs(role?: string | null): { id: VictorLang; label: string }[] {
  return role === "victor" ? VICTOR_LANGS.filter((l) => l.id !== "he") : VICTOR_LANGS;
}

/** Synchronously read the last-known role from cache (null if unknown / SSR).
 *  Lets the page restore the owner/Victor shell before paint so no owner-only
 *  areas flash in/out during the async /api/me round-trip. */
export function getCachedVictorRole(): "owner" | "victor" | null {
  try {
    const r = localStorage.getItem(ROLE_KEY);
    return r === "owner" || r === "victor" ? r : null;
  } catch {
    return null;
  }
}

/** Cache the role and, for Victor, coerce a Hebrew/blank saved language to en. */
export function rememberVictorRole(role: "owner" | "victor"): void {
  try {
    localStorage.setItem(ROLE_KEY, role);
    if (role === "victor") {
      const v = localStorage.getItem(KEY);
      if (v !== "en" && v !== "ru") setVictorLang("en"); // he or absent → en
    }
  } catch { /* ignore */ }
}

// Month names per language (Victor never sees Hebrew months).
const MONTHS: Record<VictorLang, string[]> = {
  he: ["ינואר", "פברואר", "מרץ", "אפריל", "מאי", "יוני", "יולי", "אוגוסט", "ספטמבר", "אוקטובר", "נובמבר", "דצמבר"],
  en: ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"],
  ru: ["Январь", "Февраль", "Март", "Апрель", "Май", "Июнь", "Июль", "Август", "Сентябрь", "Октябрь", "Ноябрь", "Декабрь"],
};
/** "YYYY-MM" → localized "Month YYYY". */
export function victorMonthYear(ym: string, lang: VictorLang): string {
  const [y, m] = ym.split("-").map(Number);
  if (!y || !m || m < 1 || m > 12) return ym;
  return `${MONTHS[lang][m - 1]} ${y}`;
}

export function setVictorLang(l: VictorLang): void {
  try { localStorage.setItem(KEY, l); } catch { /* ignore */ }
  if (typeof window !== "undefined") window.dispatchEvent(new CustomEvent(EVENT, { detail: l }));
}

export function useVictorLang(): [VictorLang, (l: VictorLang) => void] {
  const [lang, setLang] = useState<VictorLang>("he");
  useIso(() => { setLang(read()); }, []); // hydrate before paint → no flash
  useEffect(() => {
    const onEvt = (e: Event) => setLang((e as CustomEvent<VictorLang>).detail);
    const onStorage = (e: StorageEvent) => { if (e.key === KEY) setLang(read()); };
    window.addEventListener(EVENT, onEvt);
    window.addEventListener("storage", onStorage);
    return () => {
      window.removeEventListener(EVENT, onEvt);
      window.removeEventListener("storage", onStorage);
    };
  }, []);
  return [lang, setVictorLang];
}

